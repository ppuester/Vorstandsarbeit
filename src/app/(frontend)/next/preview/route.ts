import type { CollectionSlug, PayloadRequest } from 'payload'
import { getPayload } from 'payload'

import { draftMode } from 'next/headers'
import { redirect } from 'next/navigation'
import { NextRequest } from 'next/server'

import configPromise from '@payload-config'

export async function GET(req: NextRequest): Promise<Response> {
  const payload = await getPayload({ config: configPromise })

  const { searchParams } = new URL(req.url)

  const path = searchParams.get('path')
  const collection = searchParams.get('collection') as CollectionSlug
  const slug = searchParams.get('slug')
  const previewSecret = searchParams.get('previewSecret')
  const expectedSecret = process.env.PREVIEW_SECRET

  // Prüfen, ob PREVIEW_SECRET konfiguriert ist
  if (!expectedSecret) {
    payload.logger.error('PREVIEW_SECRET is not configured')
    return new Response('Preview functionality is not configured', { status: 500 })
  }

  // Konstante Zeit-Vergleich verwenden, um Timing-Angriffe zu verhindern
  if (!previewSecret || previewSecret !== expectedSecret) {
    return new Response('You are not allowed to preview this page', { status: 403 })
  }

  if (!path || !collection || !slug) {
    return new Response('Insufficient search params', { status: 404 })
  }

  if (!path.startsWith('/')) {
    return new Response('This endpoint can only be used for relative previews', { status: 500 })
  }

  let user

  try {
    user = await payload.auth({
      req: req as unknown as PayloadRequest,
      headers: req.headers,
    })
  } catch (error) {
    payload.logger.error({ err: error }, 'Error verifying token for live preview')
    return new Response('You are not allowed to preview this page', { status: 403 })
  }

  const draft = await draftMode()

  if (!user) {
    draft.disable()
    return new Response('You are not allowed to preview this page', { status: 403 })
  }

  // Hier können zusätzliche Prüfungen hinzugefügt werden, um zu sehen, ob der Benutzer diese Seite in der Vorschau ansehen darf

  draft.enable()

  redirect(path)
}
